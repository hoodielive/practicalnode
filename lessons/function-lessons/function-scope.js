// make a function for add, subtract, multiply, and divide - each of these functions should accept 2 params 
// and return the sum, difference, product and quotient 

function add(a, b) {
  return a + b; 
}

function subtract(a, b) {
  return a - b;
}

function multiply(a, b) {
  return a * b; 
}

function divide(a, b) {
  return a / b; 
}

add(2, 2); 
subtract(2,2); 
multiply(2,2);
divide(2,2);
